#Day 2 Variables and Data types

# String variables (text)
name = "Telman"
city = "Baku"
print (name)
print (city)

# Number variables (Decimals, Float)
Height = 6.1
Price = 19.9
print (Height)
print (Price)


#Number variables (Integers)
age = 48
year = 2026
print ("Age", age)
print ("Year", year)

#Boolean variables (True, False)
is_Student = False
is_Working = True
print("Studen ", is_Student)
print ("Working", is_Working)

#Pring with F-String (modern way to print)
print (f"My name is {name} and I am {age} years old")
print (f"I live in {city} and I am {Height} feet tall")


#User input
user_name  = input("What is your name? ")
print (f"Hello {user_name} and welcome to Python")

#Basic Math
x=10
y=3
print (f"{x} + {y} = {x+y}")
print (f"{x}-{y} = {x-y}")
print (f"{x}*{y}={x*y}")
print (f"{x}/{y} = {x/y}")
print (f"{x}%{y}= {x%y}")
print (f"{x}**{y} = {x**y}")

#Changing variables
count = 5
print(f"Count: {count}")
count +=1
print (f"Count after adding 1: {count}")
count +=5
print(f"Count after adding 5: {count}")


